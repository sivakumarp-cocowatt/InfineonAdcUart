/*-----------------------------------------------------Includes------------------------------------------------------*/
/*********************************************************************************************************************/
#include "DSADC.h"
#include "Ifx_Types.h"
#include "IfxDsadc_Dsadc.h"
#include "IfxCpu.h"

/*********************************************************************************************************************/
/*------------------------------------------------------Macros-------------------------------------------------------*/
/*********************************************************************************************************************/
#define DECIMATION_FACTOR   1024      /* Configure the oversampling rate                 */
#define START_VALUE         1024      /* Configure start value of the decimation counter */

#define MODULATOR_MASK      0x3F    /* Configure the mask of the modulator             */
#define CHANNEL_MASK        0x3F    /* Configure the mask of the channel               */

/*********************************************************************************************************************/
/*-------------------------------------------------Global variables--------------------------------------------------*/
/*********************************************************************************************************************/
/* DSADC handler data structure and channel handler structure */
IfxDsadc_Dsadc g_dsadc;
IfxDsadc_Dsadc_Channel g_dsadcChannel;

/* Variables to store the result */
float32 g_resultDSADC = 0.0;
float32 g_resultVoltage = 0.0;

float32 g_supplyVoltage = 3.3;
float32 g_maxValue = 21613.0;

/* Note: This output range (g_maxValue) is a theoretical one and it does not consider the gain or offset error */

sint8 g_chn = 0; /* Using the channel 1 */

/*********************************************************************************************************************/
/*---------------------------------------------Function Implementations----------------------------------------------*/
/*********************************************************************************************************************/
/* In this function, the DSADC module is initialized */
void init_DSADC(void)
{
    /* Initialize an instance of IfxDsadc_Dsadc_Config with default values */
    IfxDsadc_Dsadc_Config dsadcConfig;
    IfxDsadc_Dsadc_initModuleConfig(&dsadcConfig, &MODULE_DSADC);

    /* Initialize the DSADC module */
    IfxDsadc_Dsadc_initModule(&g_dsadc, &dsadcConfig);

    /* Create the channel configuration */
    IfxDsadc_Dsadc_ChannelConfig dsadcChannelConfig;
    IfxDsadc_Dsadc_initChannelConfig(&dsadcChannelConfig, &g_dsadc);

    /* Modify default configuration of the channel */
    dsadcChannelConfig.combFilter.decimationFactor  = DECIMATION_FACTOR;
    dsadcChannelConfig.combFilter.startValue = START_VALUE;
    dsadcChannelConfig.combFilter.combFilterShift = IfxDsadc_MainCombFilterShift_shiftBy3,
    dsadcChannelConfig.firFilter.fir0Enabled = TRUE;
    dsadcChannelConfig.firFilter.fir1Enabled = TRUE;
    dsadcChannelConfig.firFilter.dataShift = IfxDsadc_FirDataShift_shiftBy1;

    /* Initialize channel */
    dsadcChannelConfig.channelId = (IfxDsadc_ChannelId)g_chn;
    IfxDsadc_Dsadc_initChannel(&g_dsadcChannel, &dsadcChannelConfig);
    IfxDsadc_Dsadc_startScan(&g_dsadc, MODULATOR_MASK, CHANNEL_MASK);
}



/* This function gets the result of the most recent conversion */
void run_DSADC(void)
{
    g_resultDSADC = IfxDsadc_Dsadc_getMainResult(&g_dsadcChannel);
    g_resultVoltage = (g_resultDSADC * g_supplyVoltage) / g_maxValue;
}

/* This function gets the result of the most recent conversion */
float32 Get_DSADCbits(void)
{
    g_resultDSADC = IfxDsadc_Dsadc_getMainResult(&g_dsadcChannel);
    return g_resultDSADC;
}

/* This function gets the result of the most recent conversion */
float32 Get_DSADCVoltage(void)
{
    g_resultVoltage = (g_resultDSADC * g_supplyVoltage) / g_maxValue;
    return g_resultVoltage;
}
